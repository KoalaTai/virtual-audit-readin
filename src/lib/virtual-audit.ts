export type RegulatoryStandard = keyof typeof REGULATORY_CHECKLISTS;

export interface ClauseResult {
  clause: string;
  title: string;
  keywords: string[];
  foundKeywords?: string[];
}

export interface AuditResult {
  standard: RegulatoryStandard;
  documentPreview: string;
  timestamp: string;
  covered_clauses: ClauseResult[];
  potential_gaps: ClauseResult[];
  coveragePercentage: number;
}

export function performVirtualAudit(text: string, regulationStandard: RegulatoryStandard): AuditResult {
  const clauses = REGULATORY_CHECKLISTS[regulationStandard];
  const lowerText = text.toLowerCase();
  
  const covered_clauses: ClauseResult[] = [];
  const potential_gaps: ClauseResult[] = [];
  
  clauses.forEach(clause => {
    const foundKeywords = clause.keywords.filter(keyword => 
      lowerText.includes(keyword.toLowerCase())
    );
    
    if (foundKeywords.length > 0) {
      covered_clauses.push({
        ...clause,
        foundKeywords
      });
    } else {
      potential_gaps.push(clause);
    }
  });
  
  const coveragePercentage = Math.round((covered_clauses.length / clauses.length) * 100);
  
  return {
    standard: regulationStandard,
    documentPreview: text.substring(0, 200) + (text.length > 200 ? '...' : ''),
    timestamp: new Date().toISOString(),
    covered_clauses,
    potential_gaps,
    coveragePercentage
  };
}